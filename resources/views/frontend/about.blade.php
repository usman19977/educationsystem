                <div class="about-text">
                    <h4>1950</h4>
                    <blockquote>
                        " By the promulgation of the Central Legislative Act No. XVI of 1950. The Board of Secondary Education Karachi was established in the year 1950. Thus the control of Secondary Education was handed over to the Board of Secondary Education Karachi.Mr. Sultan Mohyuddin was appointed its first President.<br><br>

                        Initially the Board started functioning in a three roomed ground floor of a residential house at Fatima Jinnah Colony, off Jamshed Road , with a skeleton staff of 3 or 4 persons only. "
                    </blockquote>


                     <h4>1951 - 1956</h4>
                    <blockquote>
                        " The Board held its first examination for 1,461 examinees. The curriculum and courses of studies were prescribed by the University of Sindh which in itself was an adaptation of curriculum and syllabi of the Bombay University in 1951.<br><br>

                        The Board drew up its own curriculum &amp; syllabi in 1952.Mr. A.F.M Abdul Haque was appointed in 1953, as Second President and Committees of Finance, Examination and Recognition were constituted.In 1954 Heads of schools were advised to issue certificates in non-examination subjects on the basis of Cumulative Records. The Board office was shifted from F.J.Colony, Off Jamshed Road, to Bilquis Mahal, a rented building at 1st Chowrangi, Nazimabad, in November 1955 .Dr Akhtar Hussain Raipuri was appointed the third President of Board.<br><br>

                        The first examination was conducted by the Board under the own curriculum and syllabi, namely Secondary School Leaving General and Secondary School Leaving Technical Examinations in 1956.The Board undertook publication of a set of books in English, Urdu, History &amp; Geography books were published in collaboration with Silver Burdett Company of U.S.A.The Board organized a comprehensive symposium, on Secondary Education which persuaded Educators &amp; Educational Administrators to think seriously on educational. "
                    </blockquote>

                     <h4>1957 - 1959</h4>
                    <blockquote>
                        " Dr. Surat Ali Khan was appointed the 5th President of the Secondary Board. The Board Office was shifted from Bilquis Mahal, Nazimabad to PECHS in two bungalows, one at Sir Syed Road and the other at Khalid Bin Waleed Road. National Education Commission was formed and curriculum was formulated in five groups: Science, Humanities, Commerce, Home Economics and Industrial Arts.<br><br>

                        Nine medical officers were appointed in various localities to look after the health of the employees of the Board and their families. System of External Evaluation was introduced in the ratio of 75:25 respectively. Research and Evaluation Cells were established within the Boards. "
                    </blockquote>

